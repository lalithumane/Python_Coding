def add_numbers(*nums):
    total = 0
    for i in nums:
        total = total + i
    
    return total

def subract(a,b):
    return a - b


def multiple(a,b):
    return a * b

def division(a,b):
    return a / b

def remainder(a,b):
    return a % b

def power(a,b):
    return a ** b